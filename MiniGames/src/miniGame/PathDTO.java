package miniGame;


public class PathDTO {

	private String path;
	private String name;
	private String singer;
	
	public PathDTO(String path, String name, String singer) {
		this.path = path;
		this.name = name;
		this.singer = singer;
	}

	public String getPath() {
		return path;
	}

	public String getName() {
		return name;
	}

	public String getSinger() {
		return singer;
	}
	
	
	
	
	
	
	
	
	
}
